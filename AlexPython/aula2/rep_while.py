# While -> Enquanto
numero = 1
while numero < 5:
    print(numero)

    #numero = numero + 1
    numero +=1
    
    
i = 1
while i < 6:
    print(i)
    if i == 3:
        break
    i += 1
    
z = 0
while z < 6:
    # z = z + 1
    z += 1
    if z == 3:
        continue
    print(z)